    public void provjeraSigurnostiLozinke() {
        String lozinka = lozinka_registracija_JPasswordField.getText();   
        
        boolean uppercase = false;
        boolean lowercase = false;
        boolean specialcharacters = false;
        boolean digits = false;
        
        char[] Password = lozinka.toCharArray();
        
        for(int i = 0; i < lozinka.length(); i++){
            
            if(Character.isUpperCase(Password[i]))
                uppercase = true;
            
            if(Character.isLowerCase(Password[i]))
                lowercase = true;
            
            if(Character.isDigit(Password[i]))
                digits = true;
            
            if(lozinka.contains("~") || lozinka.contains("!") || lozinka.contains("@") ||
               lozinka.contains("#") || lozinka.contains("$") || lozinka.contains("%") ||
               lozinka.contains("^") || lozinka.contains("&") || lozinka.contains("*") ||
               lozinka.contains("+") || lozinka.contains("-") || lozinka.contains("/") ||
               lozinka.contains("(") || lozinka.contains(")") || lozinka.contains("<") ||
                    lozinka.contains(">"))
                specialcharacters = true;
                 
        }
        
        if(lozinka.length() < 6){
            JOptionPane.showMessageDialog(null, "Vaša lozinka je pre kratka!\nMora imati barem 6 znakova!", "Sigurnost lozinke", JOptionPane.INFORMATION_MESSAGE);
            return;
        }  
        if(lozinka.length() >= 6 && uppercase && lowercase && digits && specialcharacters){
            JOptionPane.showMessageDialog(null, "Vaša lozinka je jaka!", "Sigurnost lozinke", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        
        if ((lozinka.length() >= 6 && ((uppercase && lowercase) || digits && specialcharacters))
            &&
            (lozinka.length() >= 6 && ((uppercase && lowercase) && digits || specialcharacters))){
            JOptionPane.showMessageDialog(null, "Vaša lozinka je osrednja!\nMožete ju probati jos ojačati!", "Sigurnost lozinke", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        
        if(lozinka.length() >= 6 && (uppercase || lowercase || digits || specialcharacters)){
            JOptionPane.showMessageDialog(null, "Vaša lozinka je slaba!\nBilo bi dobro da ju promijenite da bude jača!", "Sigurnost lozinke", JOptionPane.INFORMATION_MESSAGE);
        }
    }

