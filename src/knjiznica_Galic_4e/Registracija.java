    private void stvori_registracija_JButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_stvori_registracija_JButtonActionPerformed

        if(korisnicko_ime_registracija_JTextField.getText().isEmpty() || ime_registracija_JTextField.getText().isEmpty() ||
           prezime_registracija_JTextField.getText().isEmpty() || lozinka_registracija_JPasswordField.getText().isEmpty() ||
           odgovor_registracija_JTextField.getText().isEmpty()){
            
            JOptionPane.showMessageDialog(null, "Morate ispuniti sva polja!");   
        }
        
        else{
        
        //stvaranje novog računa
        try{
            //SQL upit
            String sql = "INSERT INTO korisnik (username_korisnik, ime_korisnik, prezime_korisnik, lozinka_korisnik, pitanje_korisnik, odgovor_korisnik) VALUES (?, ?, ?, ?, ?, ?)";
            pst = conn.prepareStatement(sql);
            pst.setString(1, korisnicko_ime_registracija_JTextField.getText());
            pst.setString(2, ime_registracija_JTextField.getText());
            pst.setString(3, prezime_registracija_JTextField.getText());
            pst.setString(4, lozinka_registracija_JPasswordField.getText());
            pst.setString(5, (String) pitanje_JComboBox.getSelectedItem());
            pst.setString(6, odgovor_registracija_JTextField.getText());
            
            pst.execute();
            pst.close();
            
            JOptionPane.showMessageDialog(null, "Novi korisnički račun uspješno stvoren!");
            
            conn.close();
            
        }catch(HeadlessException | SQLException e){
            JOptionPane.showMessageDialog(null, e);
        }
      }
    }//GEN-LAST:event_stvori_registracija_JButtonActionPerformed

